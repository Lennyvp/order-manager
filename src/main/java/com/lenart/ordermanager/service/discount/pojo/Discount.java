package com.lenart.ordermanager.service.discount.pojo;

public class Discount {

    private String code;
    private int value;

    public Discount() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
